<script>
import swal from 'sweetalert2';
window.Swal = swal;

export default{
    data(){
        return {

            products: [],
            product: {
                id: '',
                title: '',
                tagline: '',
                icon: '',
                price: '',
                description: '',
                website: '' ,
                media1: '',
                media2: '',

            },
            add: true,
            edit: false,
            pagination: {},
            carts: [],

            cartadd: {
              id: '',
              title: '',
              tagline: '',
              icon: '',
              price: '',
              description: '',
              website: '' ,
              media1: '',
              media2: '',
            },

            badge: '0',
            quantity: '1',
            totalprice: '0',
            search: '',
            showsearch: false,
            caris: []
        }
    },
    created(){
        this.viewProduct();
        this.viewCart();
    },
    methods: {

        searchProduct(){
            fetch('http://127.0.0.1:8000/api/product/search?q='+this.search)
            .then(res => res.json())
            .then(res => {
                this.caris = res;
                this.search = '';
                this.showsearch = true;
            })
            .catch(err => {
                console.log(err);
            });
        },

        viewCart(){
            if(localStorage.getItem('carts')){
                this.carts = JSON.parse(localStorage.getItem('carts'));
                this.badge = this.carts.length;
                this.totalprice = this.carts.reduce((total, item)=>{
                    return total + item.price;
                }, 0);
            }
        },

        addCart(pro){
           this.cartadd.id = pro.id;
           this.cartadd.title = pro.title;
           this.cartadd.tagline = pro.tagline;
           this.cartadd.icon = pro.icon;
           this.cartadd.price = pro.price;
           this.cartadd.description = pro.description;
           this.cartadd.website = pro.website;
           this.cartadd.media1 = pro.media1;
           this.cartadd.media2 = pro.media2;
           this.cartadd.file = pro.file;

            this.carts.push(this.cartadd);
            this.cartadd = {};
            this.storeCart();
        },

        removeCart(pro){
            this.carts.splice(pro, 1);
            this.storeCart();
        },

        storeCart(){
            let parsed = JSON.stringify(this.carts);
            localStorage.setItem('carts', parsed);
            this.viewCart();
        },

       /* removeSlash(link){
          var b=JSON.stringify(link);
          var str = b.replace(/\\/g, '');
          return str;
        },*/

        viewProduct(pagi){
            pagi = pagi || 'http://127.0.0.1:8000/api/products';
            fetch(pagi)
            .then(res => res.json())
            .then(res => {
                this.products = res.data;
                this.pagination = {
                    current_page: res.meta.current_page,
                    last_page: res.meta.last_page,
                    from_page: res.meta.from,
                    to_page: res.meta.to,
                    total_page: res.meta.total,
                    path_page: res.meta.path+"?page=",
                    first_link: res.links.first,
                    last_link: res.links.last,
                    prev_link: res.links.prev,
                    next_link: res.links.next
                };
            })
            .catch(err => console.log(err));
        },

        addProduct(){
            fetch('http://127.0.0.1:8000/api/products', {
                method: 'post',
                body: JSON.stringify(this.product),
                headers: {

                    'content-type': 'application/json',
                    'Access-Control-Allow-Headers' : 'Accept',
                    'Cross-Origin-Resource-Policy': 'same-origin'

                }
            })
            .then(res => res.json())
            .then(data => {
              //toast("Successful!", "Product has been added", "success");
                this.product.title= '';
                this.cartadd.tagline = '';
                this.cartadd.icon = '';
                this.product.price = '';
                this.product.description = '';
                this.cartadd.website = '';
                this.cartadd.media1 = '';
                this.cartadd.media2 = '';
                this.cartadd.file = '';
               // this.product.amount = '';
                this.viewProduct();
            })
            .catch(err => {
              //toast("Failed!", "Product fail to add", "error");
            });
        },

        editProduct(pro){
            this.add = false;
            this.edit = true;
            this.cartadd.id = pro.id;
            this.cartadd.title = pro.title;
            this.cartadd.tagline = pro.tagline;
            this.cartadd.icon = pro.icon;
            this.cartadd.price = pro.price;
            this.cartadd.description = pro.description;
            this.cartadd.website = pro.website;
            this.cartadd.media1 = pro.media1;
            this.cartadd.media2 = pro.media2;
          this.cartadd.file = pro.file;
           // this.product.amount = pro.amount
        },

        updateProduct(id){
            fetch(`http://127.0.0.1:8000/api/products/${id}`, {
                method: 'put',
                body: JSON.stringify(this.product),
                headers: {
                    'content-type': 'application/json' ,
                    'Access-Control-Allow-Headers' : '*'
                }
            })
            .then(res => res.json())
            .then(data => {
             // toast("Successful!", "Product has been updated", "success");
                this.add = true;
                this.edit = false;
                this.product.title= '';
                this.cartadd.tagline = '';
                this.cartadd.icon = '';
                this.product.price = '';
                this.product.description = '';
                this.cartadd.website = '';
                this.cartadd.media1 = '';
                this.cartadd.media2 = '';
                this.cartadd.file = '';
            })
            .catch(err => {
              //toast("Failed!", "Product fail to update", "error");
            });
        },

        deleteProduct(id){
          swal({
                title: "Are you sure?",
                text: "Product will be delete",
                icon: "warning",
                buttons: true,
                dangerMode: true
            })
            .then((willdelete)=>{
                if(willdelete){

                    fetch(`http://127.0.0.1:8000/api/products/${id}`, {
                      method: 'delete',
                      headers: {
                        'content-type': 'application/json' ,
                        'Access-Control-Allow-Headers' : '*'
                      }
                    })
                    .then(res => res.json())
                    .then(data => {
                     // toast("Successful!", "Product has been deleted", "success");
                        this.viewProduct();
                    })
                    .catch(err => {
                      //toast("Failed!", "Product fail to delete", "error");
                    });

                }
            })
        },
        clearProduct(){
            this.add = true;
            this.edit = false;
            this.product.id = '';
            this.product.title= '';
            this.cartadd.tagline = '';
            this.cartadd.icon = '';
            this.product.price = '';
            this.product.description = '';
            this.cartadd.website = '';
            this.cartadd.media1 = '';
            this.cartadd.media2 = '';
            this.cartadd.file = '';

            // this.product.amount = '';
        },
        viewLink(mylink){
            location.href = 'http://127.0.0.1:8000/detail/'+mylink;
        }
    }

}
</script>

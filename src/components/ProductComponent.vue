                                            <button @click="removeCart(n)" class="btn btn-danger btn-sm"><i class="fa fa-close"></i></button>
                                        </td>
                                    </tr>
                                </tbody>
                            </table>
                        </div>
                        <div class="modal-footer">
                            Total Price: $. {{totalprice}} &nbsp;
                            <a href="http://127.0.0.1:8000/checkout" class="btn btn-primary">Checkout</a>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>
    <p><br/></p>
    <div class="row">
        <div class="col-md-8">
            <div v-if="showsearch===true">
                <div class="row">
                    <div v-for="cari in caris" v-bind:key="cari.id" class="col-md-6">
                        <div class="card mb-4">

                            <img :src="cari.icon" :alt="cari.title" class="card-img-top">

                            <div class="card-body text-right">
                                <h4 style="color:#000000 !important;">{{ cari.title }}</h4>
                                <p  style="color:#000000 !important;">{{ cari.description }}</p>

                                <div class="row">
<!--                                    <div class="col-md-6">Stock {{ cari.amount }}</div>-->
                                    <div class="col-md-6 text-right">$. {{ cari.price }}</div>
                                </div>
                                <p class="text-right mt-2">
                                    <button @click="viewLink(cari.id)" class="btn btn-primary">View</button>
                                    <button @click="editProduct(cari)" class="btn btn-warning">Edit</button>
                                    <button @click="deleteProduct(cari.id)" class="btn btn-danger">Delete</button>
                                </p>
                            </div>
                        </div>
                    </div>
                </div>
            </div>
            <div v-if="showsearch==false">
            <div class="row">
            <div v-for="product in products" v-bind:key="product.id" class="col-md-6">
                <div class="card mb-4">
                    <img class="card-img-top" :src="product.icon" :alt="product.title"/>
                    <div class="card-body">

                        <h4 style="color:#000000 !important;">{{ product.title }}</h4>
                        <p  style="color:#000000 !important;">{{ product.description }}</p>

                        <div class="row">
